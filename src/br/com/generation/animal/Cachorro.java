package br.com.generation.animal;

public class Cachorro extends Animal{

	private String correr;
	private String som;
	private String auauCachorro;
	
	

	public String getCorrer() {
		return correr;
	}



	public String getSom() {
		return som;
	}



	public void setSom(String som) {
		this.som = som;
	}



	public String getAuauCachorro() {
		return auauCachorro;
	}



	public void setAuauCachorro(String auauCachorro) {
		this.auauCachorro = auauCachorro;
	}



	}

